package com.example.usernotifs.models;

public class User {
    private long id;
    private String name;
    private String surname;
    private int age;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getAge() {
        return age;
    }
}
